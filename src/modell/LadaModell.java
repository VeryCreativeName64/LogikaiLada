package modell;


public class LadaModell {
    private String anyag;
    private boolean bennevan;

    public LadaModell(String anyag, boolean bennevan) {
        this.anyag = anyag;
        this.bennevan = bennevan;
    }

    public String getAnyag() {
        return anyag;
    }

    public boolean isBennevan() {
        return bennevan;
    }
    
    

    

    
    
}
